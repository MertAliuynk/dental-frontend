        try {
          groups.forEach((g) => {
            const evt = new Event('reposition');
            // no-op, sadece effect baştan çalışsın diye basitçe fonksiyonu çağırıyoruz
          });
          // Basit yol: fonksiyonu yeniden çağır
          // Not: burada reentrancy basit tutuluyor; performans kritik değil
        } catch {}
